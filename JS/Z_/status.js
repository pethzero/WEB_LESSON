// ตัวแปรสำหรับเก็บสถานะต่าง ๆ
const STATUS = {
    NEW: 10, // สถานะใหม่ (ค่าระหว่าง 1-10)
    ACTIVE: 50, // สถานะที่กำลังใช้งาน (ค่าระหว่าง 41-60)
    DELETE: 0, // สถานะที่ถูกลบ (ค่า 0)
    DEACTIVE: 80, // สถานะที่ไม่ใช้งาน (ค่า 80)
    FINISH: 100, // สถานะที่เสร็จสิ้น (ค่า 100)
    INACTIVE: 25, // สถานะที่ไม่ได้ใช้งาน (ค่าระหว่าง 21-30)
    PENDING: 60, // สถานะที่รอดำเนินการ (ค่าระหว่าง 61-70)
    CANCELLED: -10, // สถานะที่ถูกยกเลิก (ค่า -10)
    ARCHIVED: 95, // สถานะที่ถูกเก็บถาวร (ค่า 95)
    ON_HOLD: 45, // สถานะที่ถูกพักการดำเนินการ (ค่า 45)
    PENDING_APPROVAL: 65 // สถานะที่รอการอนุมัติ (ค่าระหว่าง 61-70)
};
// ตัวอย่างการใช้งาน
let currentStatus = STATUS.NEW;

console.log("Current status:", currentStatus); // แสดงผล Current status: 10

// เปลี่ยนสถานะไปเป็น ACTIVE
currentStatus = STATUS.ACTIVE;

console.log("Current status:", currentStatus); // แสดงผล Current status: 50

// เปลี่ยนสถานะไปเป็น DELETE
currentStatus = STATUS.DELETE;

console.log("Current status:", currentStatus); // แสดงผล Current status: 0

// เปลี่ยนสถานะไปเป็น INACTIVE
currentStatus = STATUS.INACTIVE;

console.log("Current status:", currentStatus); // แสดงผล Current status: 25


// แน่นอนครับ! ดังนั้นเราจะเรียงลำดับหัวข้อตามที่กล่าวถึงได้แก่:

// 1. **New (ใหม่)**: ค่าเริ่มต้นที่ต่ำที่สุด เช่น 1 หรือ 10
// 2. **Active (ใช้งาน)**: ค่าที่สูงขึ้นจาก New เช่น 50 หรือ 75
// 3. **Delete (ลบ)**: ค่าที่ไม่ถูกใช้อีก เช่น -1 หรือ 0
// 4. **Deactive (ไม่ใช้งาน)**: ค่าที่อยู่ระหว่าง Active และ Finish เช่น 80
// 5. **Finish (เสร็จสิ้น)**: ค่าสูงสุด เช่น 100
// 6. **เสีย (Inactive)**: สถานะที่บ่งบอกว่าสิ่งนั้นไม่ได้ถูกใช้งานอยู่ในขณะนั้น เช่น 25 หรือ 30
// 7. **Pending (รอดำเนินการ)**: สถานะนี้ใช้เพื่อระบุว่ารายการหรือข้อมูลนั้นยังไม่ได้ดำเนินการเสร็จสิ้น เช่น 50 หรือ 60
// 8. **Cancelled (ยกเลิก)**: สถานะที่บ่งบอกว่าการดำเนินการหรือรายการนั้นถูกยกเลิก และไม่ได้ทำการต่อไป เช่น -10 หรือ -20
// 9. **Archived (เก็บถาวร)**: สถานะที่บ่งบอกว่าข้อมูลหรือรายการนั้นถูกเก็บไว้เพื่อการอ้างอิงหรือการเก็บข้อมูลเท่านั้น เช่น 90 หรือ 95
// 10. **On Hold (พักการดำเนินการ)**: สถานะที่บ่งบอกว่าการดำเนินการนั้นอยู่ในสถานะที่ถูกพักไว้ชั่วคราว เช่น 40 หรือ 45
// 11. **Pending Approval (รอการอนุมัติ)**: สถานะที่บ่งบอกว่ารายการหรือข้อมูลนั้นถูกส่งไปรอการอนุมัติก่อนที่จะดำเนินการต่อไป เช่น 55 หรือ 65

// โดยการเรียงลำดับเช่นนี้จะทำให้การจัดการข้อมูลในระบบหรือฐานข้อมูลของคุณมีระเบียบมากขึ้นและเข้าใจง่ายขึ้นสำหรับผู้ใช้งานทั่วไปด้วยครับ