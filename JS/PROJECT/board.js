function processMaterialGroup(query_result) {
    let mat_group = {};  // สร้าง object เพื่อเก็บข้อมูล

    // วนลูปผ่าน query_result (ที่เป็น JSON array)
    for (let item of query_result) {
        let parent_id = item['material_no'];
        let com_value = item['component'];
        let comp_qty = item['comp_qty'];

        // ตัดคำเพื่อให้ได้รูปแบบ '01E', '01S', หรือ '02P'
        let com_prefix = com_value.split('-')[0];  // ตัดคำที่เครื่องหมาย '-'

        if (!(parent_id in mat_group)) {
            mat_group[parent_id] = {
                index: 0,
                detail: []  // เริ่มต้นเป็น array ว่าง
            };
        }

        // ตรวจสอบว่ามีคำว่า '01E' หรือ '01S' ใน com_prefix
        if (com_prefix.includes('01E') || com_prefix.includes('01S')) {
            // ถ้า detail ยังไม่มีข้อมูล ให้เพิ่ม object ว่างก่อน
            if (mat_group[parent_id]['detail'].length === 0) {
                mat_group[parent_id]['detail'].push({});
            }

            // ถ้ามีการจับคู่แล้ว ให้เพิ่มรายการใหม่
            if ('sub' in mat_group[parent_id]['detail'][mat_group[parent_id]['detail'].length - 1]) {
                mat_group[parent_id]['index'] += 1;
                mat_group[parent_id]['detail'].push({});
            }

            mat_group[parent_id]['detail'][mat_group[parent_id]['detail'].length - 1]['main'] = com_value;

        } else if (com_prefix.includes('02P')) {
            // ถ้า detail ยังไม่มีข้อมูล ให้เพิ่ม object ว่างก่อน
            if (mat_group[parent_id]['detail'].length === 0) {
                mat_group[parent_id]['detail'].push({});
            }

            // ถ้า main ยังไม่มี ให้ตั้งเป็น parent_id
            if (!('main' in mat_group[parent_id]['detail'][mat_group[parent_id]['detail'].length - 1])) {
                mat_group[parent_id]['detail'][mat_group[parent_id]['detail'].length - 1]['main'] = parent_id;
            }

            // ถ้า sub ยังไม่มี ให้เพิ่มค่า sub
            if (!('sub' in mat_group[parent_id]['detail'][mat_group[parent_id]['detail'].length - 1])) {
                mat_group[parent_id]['detail'][mat_group[parent_id]['detail'].length - 1]['sub'] = com_value;
                mat_group[parent_id]['detail'][mat_group[parent_id]['detail'].length - 1]['comp_qty'] = comp_qty;
            } else {
                // ถ้ามีทั้ง main และ sub แล้ว ให้เพิ่มรายการใหม่
                mat_group[parent_id]['index'] += 1;
                mat_group[parent_id]['detail'].push({
                    'main': parent_id,
                    'sub': com_value,
                    'comp_qty': comp_qty
                });
            }
        }
    }

    return mat_group;  // คืนค่า mat_group ที่ประมวลผลแล้ว
}
const mat_group = processMaterialGroup(query_result);

// ผลลัพธ์ของข้อมูล
function extractData(mat_group) {
    let extracted_data = [];  // สร้าง array เพื่อเก็บข้อมูลที่ดึงออกมา

    // วนลูปผ่าน mat_group
    for (const [parent_id, data] of Object.entries(mat_group)) {
        let row_dt = 1;  // ตัวแปรสำหรับบันทึกกลุ่ม

        // วนลูปผ่านรายละเอียดใน 'detail'
        for (const detail of data['detail']) {
            if ('sub' in detail) {
                // เพิ่มข้อมูลลงใน extracted_data
                extracted_data.push({
                    'plant': 'SVI2',
                    'fg_material_no': parent_id,
                    'component_main': detail['main'],
                    'component_detail': detail['sub'],
                    'comp_qty': detail['comp_qty'],
                    'group_m': row_dt,
                    'create_date': new Date().toISOString() // ใช้เวลาปัจจุบัน
                });

                row_dt += 1;  // เพิ่มค่า row_dt ทีละ 1
            }
        }
    }

    return extracted_data;  // คืนค่า array ที่ดึงข้อมูลแล้ว
}


